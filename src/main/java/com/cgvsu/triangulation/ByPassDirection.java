package com.cgvsu.triangulation;

/**
 * Направление обхода вершин полигона при триангуляции.
 * 
 * <p>Используется для определения порядка обработки вершин
 * при разбиении полигона на треугольники.
 * 
 * @author CGVSU Team
 * @version 1.0
 */
public enum ByPassDirection {
    /**
     * Первое направление обхода (обычно по часовой стрелке).
     */
    FIRST,
    
    /**
     * Второе направление обхода (обычно против часовой стрелки).
     */
    SECOND
}
